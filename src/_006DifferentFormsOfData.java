public class _006DifferentFormsOfData
{
    public static void main(String[] args)
    {
        int a = -5;
        System.out.println("Value of variable 'a' in Decimal is : "+"'"+a+"'");
        System.out.println("Value of variable 'a' in Binary is : "+"'"+Integer.toBinaryString(a)+"'");
        System.out.println("Value of variable 'a' in Hexadecimal is : "+"'"+Integer.toHexString(a)+"'");
        System.out.println("Value of variable 'a' in Octal is : "+"'"+Integer.toOctalString(a)+"'");
    }
}
